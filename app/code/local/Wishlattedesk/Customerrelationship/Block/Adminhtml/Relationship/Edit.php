class Wishlattedesk_Customerrelationship_Block_Adminhtml_Relationship_Edit extends Mage_Adminhtml_Block_Widget_Form_Container
{
    public function __construct()
    {
        parent::__construct();
                 
        $this->_objectId = 'id';
        $this->_blockGroup = 'customerrelationship';
        $this->_controller = 'adminhtml_relationship';

        $this->_addButton('saveandcontinue', array(
            'label'     => Mage::helper('adminhtml')->__('Save And Continue Edit'),
            'onclick'   => 'saveAndContinueEdit()',
            'class'     => 'save',
        ), -100);


        $this->_formScripts[] = "
            function saveAndContinueEdit(){
                editForm.submit($('edit_form').action+'back/edit/');
            }
        ";
    }

    public function getHeaderText()
    {
        if( Mage::registry('relationship_data') && Mage::registry('relationship_data')->getId() ) {
            return Mage::helper('customerrelationship')->__("Edit Relationship '%s'", $this->htmlEscape(Mage::registry('relationship_data')->getTitle()));
        } else {
            return Mage::helper('customerrelationship')->__('Add Relationship');
        }
    }
}